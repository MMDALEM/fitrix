  // پرکردنِ یک <select> با فهرست
  function fill(select, list, placeholder, selected) {
    if (!select) return;
    select.innerHTML =
      '<option value="">' + (placeholder || "انتخاب کنید") + "</option>";
    (list || []).forEach(function (name) {
      var opt = document.createElement("option");
      opt.value = name;
      opt.textContent = name;
      if (selected && selected === name) opt.selected = true;
      select.appendChild(opt);
    });
  }

  // راه‌اندازیِ آبشاری: اول استان، سپس شهر
  // stateSel / citySel: عنصر select یا id آن‌ها
  window.initProvinceCity = function (stateSel, citySel, selState, selCity) {
    var s =
      typeof stateSel === "string" ? document.getElementById(stateSel) : stateSel;
    var c =
      typeof citySel === "string" ? document.getElementById(citySel) : citySel;
    if (!s || !c) return;

    var provinces = Object.keys(window.IRAN_PROVINCES);
    fill(s, provinces, "استان را انتخاب کنید", selState || "");

    function refreshCities(keepSelected) {
      var cities = window.IRAN_PROVINCES[s.value] || [];
      c.disabled = !s.value;
      fill(c, cities, s.value ? "شهرستان را انتخاب کنید" : "ابتدا استان را انتخاب کنید", keepSelected || "");
    }

    // مقدار اولیه (حالت ویرایش)
    if (selState) refreshCities(selCity || "");
    else {
      c.disabled = true;
      fill(c, [], "ابتدا استان را انتخاب کنید", "");
    }

    s.addEventListener("change", function () {
      refreshCities("");
    });
  };
